package com.dl.OperatorOverloading;

//One operator can act as more than one form is called Operator Loading

public class Eg1 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		System.out.println(a + b);
		
		String s1 = "10";
		String s2 = "20";
		System.out.println(s1 + s2);
	}

}
